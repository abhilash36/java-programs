package beforeoops;

public class IncAndDecOperator {

	public static void main(String[] args) 
	{
		int a=20;
		
		System.out.println("Post increment: "+a++); //20
		System.out.println("Pre increment: "+ ++a); //22
		System.out.println("Post decrement: "+ a--); //22
		System.out.println("Pre decrement: "+ --a); //20

	}

}
